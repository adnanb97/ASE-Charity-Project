using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using CharityData.Models;
using CharityProject;

namespace CharityProject.Controllers
{
    public class OrganizationsController : Controller
    {
        private readonly CharityContext _context;

        public OrganizationsController(CharityContext context)
        {
            _context = context;
        }

        // GET: Organizations
        public ViewResult Index()
        {
            var account = new Account() { Id = 1, imageId = new Image() { Id = 1, Path = "path" }, isUser = false, password = "pass123", username = "username1" };

            //_context.account.Add(account);

            //_context.SaveChanges();

            var orgnization = new Organization()
